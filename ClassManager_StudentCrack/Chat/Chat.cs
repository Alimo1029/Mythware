        /// <summary>
        /// 添加16位随机Hex
        /// </summary>
        /// <param name="IntArray"></param>
        /// <returns></returns>
        private static List<int> AddRandomHex(List<int> IntArray)
        {
            int[] Random = Crypto.MyRandom.Get32RandomNum();

            for (int i = 0; i < Random.Length; i++)
            {
                IntArray[12 + i] = Random[i];
            }

            return IntArray;
        }

        private static List<int> FormatString(List<int> IntArray, string Msg)
        {
            byte[] ByteMsg;

            ByteMsg = Encoding.Unicode.GetBytes(Msg);
            for (int i = 0; i < ByteMsg.Length; i++)
            {
                IntArray[56 + i] = ByteMsg[i];
            }

            return IntArray;
        }


        /// <summary>
        /// 发送信息
        /// </summary>
        /// <param name="Msg">欲发送字符串</param>
        /// <param name="Ip">对方IP</param>
        /// <param name="Port">端口号，默认4705</param>
        /// <returns>BOOL</returns>
        public static bool SendMsg(string Msg, string IP, int Port=4705)
        {// TODO: 参数变数组
            byte[] ChatByte = new byte[ChatIntArray.Count];
            List<int> ChatData = ChatIntArray;
            NetSocket.Chat chat = new NetSocket.Chat(IP, Port);

            ChatData = AddRandomHex(ChatData);  // 添加16位随机Hex
            ChatData = FormatString(ChatData, Msg);  // String转HEX
            for (int i = 0; i < ChatData.Count; i++)
            {
                ChatByte[i] = byte.Parse(ChatData[i].ToString());
            }
            try
            {
                chat.MsgSend(ChatByte);
            }
            catch (Exception)
            {
                return false;
            }

            return true;
        }
